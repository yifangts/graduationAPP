        <p>时空扫描统计量在空间扫描统计量上引入时间维，使扫描统计量能同时在空间和时间上对聚集性进行探测。利用回顾性时空扫描统计量对美国2000-2013年、欧洲2004-2013年爆发的树流感疫情地进行时空聚集性分析。由疫情地各郡“树流感”爆发点统计数据，以每个郡的空间质心作为发病点，时间步长分别设为1个月（美国）、1年（欧洲），时空聚集扫描的最大时间都为12个月，空间扫描窗口最大半径都为1000km，结果见图4。在美国，时空聚集最强的区域在加州Sonoma、Napa等郡，发生于2006年4月；在欧洲地区，时空聚集最强的区域在英格兰、苏格兰及爱尔兰等地区，发生于2013年。</p>
        <img src="/src/images/shuliugan5.jpg">
        <p class="img_explain">图4  美国/欧洲“树流感”爆发点时空聚集性专题图</p>
        <p>限制物种的潜在分布区的因素包括生物学特性、环境和人为等因素，但气候条件始终是制约物种陆地分布的最基本的因素。对于物种适生性评价的方法很多，其中AHP-模糊综合评价方法在专家知识和主观经验的基础上，利用具有严密逻辑性的数学方法较全面地综合评价主体的意见，客观反映评价对象的隶属程度，合理确定评价指标权重，用定量手段刻画适生评价中的定性问题，从而为“树流感”在中国的适生区预测提供客观准确可靠的依据。</p>
        <p class="title sub_title">我国“树流感”爆发风险有多大？</p>
        <p>根据病菌的生理生态特性，并调研总结国内外学者已有结论，研究选取的“树流感”适生评判因子为降水量、最高气温、最低气温和相对湿度4个环境变量。基于AHP-模糊综合评价方法得到“树流感”在我国的适生度分布和适生分级图。进一步把“树流感”寄主植被分布图与适生分级分布专题图进行空间叠置分析，得到“树流感”在我国的潜在分布。统计结果表明，在有林地范围内，最适宜区域面积为48.88万平方千米，中等适宜区域面积为49.38万平方千米，对这些寄主植被覆盖区域应着重关注“树流感”发生的潜在风险。</p>
