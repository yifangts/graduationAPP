        <p class="title">人会打喷嚏，树也会得“流感”，而且后果很严重</p>
        <p style='text-indent:0'>2017年04月19日 09:29</p>
        <hr>
        <p>来源：中科院之声公众号</p>
        <p>随着全球气候变化对森林生态系统的影响，以及人类生产生活的全球化以及密切的森林木材产品贸易往来，森林病虫害的爆发及传播愈发频繁，造成的经济、社会和生态危害日益加剧。其中，“树流感”就是典型代表之一。</p>
        <p class="title sub_title">究竟什么是“树流感”？</p>
        <p>“树流感”的学名是“栎树猝死病”（Sudden Oak Death， SOD），是由栎树猝死病菌引起的林木和观赏植物病害，能够在短时间内对树木造成致命伤害（枝干溃疡、枝叶枯萎），从感染到出现症状只需3-4周。引起“树流感”的栎树猝死病菌为一种类真菌，可随感病叶片、幼枝、植物苗木进行传播扩散，且植物受到感染后没有有效的防治方法。</p>
        <p>栎树猝死病菌首次于1993年在德国和荷兰的杜鹃和荚蒾上被检测出，1996年出现在美国加州沿海地区的密花石栎和栎属植物上，美国动植物健康检疫局（APHIS）于2013年发布的最新栎树猝死病菌已确认寄主及易感寄主名单，全球共发现137种（属）植株可在自然条件下受到侵染，在人工接种条件下，还有100多种植物能感染而造成叶片枯萎。可怕的是，目前寄主植物名单还在不断扩大，“树流感”既可危害阔叶树也可危害针叶树，既可危害乔木又可危害灌木，在危害苗木的同时还可危害成熟林。随着全球化发展，一个地域的栎树猝死病菌比过去更易携带或转移到另一地域，并在缺乏天敌等制约因素的新环境下扩散，导致不可逆转的生态灾难。目前该病害已引起加拿大、新西兰、欧盟、韩国、中国以及美国等多个国家地区的高度关注，纷纷采取措施严防其传入或扩散。</p>
        <img src="/src/images/shuliugan2.jpg">
        <p class="img_explain">图1  栎树猝死病菌在寄主间传播途径：①表示游动孢子通过雨水、风携带或者人类活动等到达寄主；②表示孢子侵入寄主躯干；③表示游动孢子侵入低矮乔木或灌木；④表示植被上的孢子随落叶到达地面；⑤表示地面落叶上的孢子又侵入寄主底部；⑥表示孢子掉落至地面，形成厚垣孢子在土壤中存活。（California Oak Mortality Task Force）。</p>
        <p>在此背景下，中科院遥感地球所遥感科学国家重点实验室曹春香研究员带领的环境健康遥感研究团队开展了“树流感”爆发风险遥感诊断与预警研究，并且先后得到了国家自然科学基金委和国家林业局林业公益性行业科研专项的经费资助。目前在研的林业公益性行业科研专项任务包括：基于遥感等多源数据，建立全球“树流感”环境背景因子数据集，研究“树流感”在全球的时空分布特征；以遥感和GIS等空间信息技术为主要手段分析影响“树流感”分布和传播的主要环境因子，并对其在全球的潜在适生区进行划分；根据有害生物风险评估理论，建立“树流感”的入侵风险评估指标体系和预警模型，针对“树流感”在中国的潜在入侵及传播风险进行预测预警。</p>
        <img src="/src/images/shuliugan3.jpg">
        <p class="img_explain">图2  全球“树流感”（栎树猝死病）爆发点分布图</p>
        <p>目前已知的“树流感”野外爆发点分布在美国、欧洲两地，其中，美国地区爆发点采集时间为2000-2013年，加州 Humboldt 郡的树流感爆发点数最多，共998个；其次加州 Sonoma、Marin、SanMateo、SantaClara、Monterey郡与俄勒冈州Curry郡的树流感爆发点数较多，在201-500个之间；其次为加州ContraCosta、Alameda、SantaCruz等郡，爆发点数在101-200个之间；再其次是加州Mendocino、Napa、Solano、LosAngeles、SanDiego等郡的爆发点数介于11-100个之间。欧洲地区爆发点采集时间为2004-2006年（欧洲全区调查，RAPRA协调）、2010-2013年（英国林业委员会）。英国Devon郡的树流感爆发点最多，爆发点数大于151个；其次英国Cornwall、Dumfries& Galloway等郡的爆发点数在100-151个之间；而西班牙、法国、德国、英国等国都存在爆发点数为11-50个的郡。</p>
