        <p>通过该公益专项研究，曹春香研究团队发现了喜马拉雅山脉地区邻近的中国南方地区在短期、中长期都处于高侵入风险，虽然“树流感”目前在国内尚未爆发，但其主要寄主植物在我国都有广泛的分布，一旦病菌侵入中国典型高风险区，该病害将快速扩散，并在短期内到达爆发高峰期，从而改变我国森林树种构成，导致我国森林生态系统功能严重分裂，对我国的森林资源及森林生态建设造成极大的破坏，对社会经济和人民生活质量产生巨大的影响。</p>
        <p>为了对“树流感”爆发及传播风险预测结果进行验证，曹春香研究团队在综合考虑“树流感”的气候适宜性、寄主植物的分布范围以及不同寄主对病菌的承载传播能力的基础上，选取了高、中、低风险的重庆市巫溪县、云南省屏边县、江西省泰和县、海南省白沙县4个区域作为风险预测示范区。科研人员分别于2016年11月5日至10日在巫溪、11月24日至28日在屏边、12月5日至9日在泰和，2017年3月8日至11日在白沙开展了野外调查实验。</p>
