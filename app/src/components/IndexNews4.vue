        <p>实验中调查了实验区森林样方26个，探查了各样方的寄主物种的组成、分布以及结构参数；提取了气温、降水等气候信息；采集了健康与非健康植物样本60个；测量了42种植被的叶片光谱；构建了“树流感”寄主植被光谱库。实验表明“树流感”寄主植被在风险区内的长势良好且分布广泛，风险值和寄主植被的分布一致性很高。实验中还发现在云南省屏边县的杉木爆发了类似“树流感”的枯黄病，实验小组对病株进行采样并送达有关部门进行检验。</p>
        <p>除了针对中国地区的研究，以美国和欧洲为例，该公益专项还采用了Maxent、GARP 等多种生态位模型的技术手段对全球范围的“树流感”爆发和传播风险进行了短期和中长期的预测预警。基于理论预测研究和实地调查验证发现，利用遥感等空间信息技术诊断“树流感”并对其爆发风险进行预测预警，可以获取栎树猝死病菌潜在寄主的分布状况，发现“树流感”的时空分布及传播规律，识别全球及中国“树流感”的潜在爆发高风险区，进而服务于最大限度保护全球森林植被资源，保障国家森林健康和生态安全。</p>
